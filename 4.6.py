
sentence = "ABC"

if sentence.isalnum():
    print("isalnum() ")

if sentence.isdigit():
    print("isdigit() ")

if sentence.isalpha():
    print("isalpha()")

if sentence.isspace():
    print("isspace()")

if sentence.islower():
    print("islower()")

if sentence.isupper():
    print("isupper()")



